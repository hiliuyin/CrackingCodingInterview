/*
题目描述
请编写程序交换一个数的二进制的奇数位和偶数位。（使用越少的指令越好）
给定一个int x，请返回交换后的数int。
测试样例：
10
返回：5
*/

/*
先操作奇数位，再操作偶数位，通过将10101010（即0xAA）作为掩码，提取奇数位，并将它右移1位
通过01010101（即0x5555）作为掩码，提取偶数位，并将它左移1位，
将两者做异或操作即为所求
*/

class Exchange {
public:
    int exchangeOddEven(int x) {
        return (((x & 0xAAAAAAAA) >> 1) | ((x & 0x55555555) << 1));
    }
};
